#!/usr/bin/env node
/**
 * 在 CI 中从环境变量生成 deploy.config.cjs（不写回仓库，仅 runner 内使用）。
 * 用法：node scripts/ci/write-vps-deploy-config.mjs backend|admin
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(__dirname, '..', '..');

function requireEnv(name) {
  const v = process.env[name];
  if (v === undefined || v === '') {
    throw new Error(`Missing required environment variable: ${name}`);
  }
  return v;
}

function optionalEnv(name, fallback) {
  const v = process.env[name];
  if (v === undefined || v === '') {
    return fallback;
  }
  return v;
}

const mode = process.argv[2];
if (mode !== 'backend' && mode !== 'admin') {
  console.error('Usage: node scripts/ci/write-vps-deploy-config.mjs backend|admin');
  process.exit(1);
}

const keyPath = requireEnv('DEPLOY_SSH_KEY_PATH');
if (!fs.existsSync(keyPath)) {
  throw new Error(`SSH key file not found: ${keyPath}`);
}

const host = requireEnv('DEPLOY_HOST');
const port = Number(optionalEnv('DEPLOY_SSH_PORT', '22'));
const username = requireEnv('DEPLOY_USER');
const passphrase = optionalEnv('DEPLOY_SSH_PASSPHRASE', '');

if (mode === 'backend') {
  const remotePath = requireEnv('DEPLOY_BACKEND_REMOTE_PATH');
  const backupPath = optionalEnv('DEPLOY_BACKEND_BACKUP_PATH', path.posix.join(remotePath, 'backup'));
  const pm2AppName = optionalEnv('DEPLOY_PM2_APP_NAME', 'nest_admin_soybean_server');
  const healthCheckUrl = optionalEnv('DEPLOY_HEALTH_CHECK_URL', 'http://localhost:8080/api/health');

  const outPath = path.join(workspaceRoot, 'apps', 'backend', 'scripts', 'deploy', 'deploy.config.cjs');
  const body = {
    prod: {
      name: 'GitHub Actions VPS',
      host,
      port,
      username,
      password: '',
      privateKey: keyPath,
      passphrase,
      remotePath,
      backupPath,
      isBackup: true,
      keepBackups: 5,
      includeEnvFile: false,
      runMigration: false,
      dbPush: false,
      runSeed: false,
      pm2AppName,
      healthCheckUrl,
    },
  };

  const serialized = JSON.stringify(body, null, 2);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(
    outPath,
    `// Generated by scripts/ci/write-vps-deploy-config.mjs
module.exports = ${serialized};
`,
    'utf8',
  );
  console.log('Wrote', outPath);
} else {
  const remotePath = requireEnv('DEPLOY_ADMIN_REMOTE_PATH');
  const backupPath = optionalEnv('DEPLOY_ADMIN_BACKUP_PATH', path.posix.join(remotePath, 'backup'));

  const outPath = path.join(workspaceRoot, 'apps', 'admin-web', 'scripts', 'deploy.config.cjs');
  const body = {
    prod: {
      name: 'GitHub Actions VPS (admin)',
      host,
      port,
      username,
      password: '',
      privateKey: keyPath,
      passphrase,
      distPath: 'dist',
      remotePath,
      backupPath,
      isBackup: true,
    },
  };

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(
    outPath,
    `// Generated by scripts/ci/write-vps-deploy-config.mjs
module.exports = ${JSON.stringify(body, null, 2)};
`,
    'utf8',
  );
  console.log('Wrote', outPath);
}
